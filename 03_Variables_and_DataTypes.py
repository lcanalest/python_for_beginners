# Variable types
## Decimal
sales_amount = 21341.453
print("sales_amount: " + str(sales_amount))

## Integer
sales_amount = 21341
print("sales_amount: " + str(sales_amount))

## Boolean
is_male = True
is_female = False
print("is_male: " + str(is_male))
print("is_female: " + str(is_female))

## String
name = "Sabuesito"
print("name: " + name + "\n")

## Example
character_name = "Pepe"
character_age = 35

print("There once was a man named " + character_name + ", ")
print("he was " + str(character_age) + " years old.")
print("He really liked the name " + character_name + ", ")
print("but he didn't like being " + str(character_age) + ".")